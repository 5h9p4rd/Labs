# Ввод значений a и b
a = int(input("Введите значение a (a ≤ 200): "))
b = 200  # Верхняя граница

# Проверка условия a ≤ 200
if a > 200:
    print("Ошибка: a должно быть меньше или равно 200.")
else:
    # Инициализация переменных
    total_sum = 0
    count = 0

    # Цикл for для подсчета суммы и количества чисел
    for number in range(a, b + 1):
        total_sum += number
        count += 1

    # Вычисление среднего арифметического
    average = total_sum / count

    # Вывод результата
    print(f"Среднее арифметическое чисел от {a} до {b}: {average}")


    # Ввод количества чисел в последовательности
n = int(input("Введите количество чисел в последовательности: "))

# Инициализация переменных
count_positive = 0
i = 0

# Ввод чисел и подсчет положительных в начале
while i < n:
    num = float(input(f"Введите число {i + 1}: "))
    if num <= 0:
        break  # Прерываем цикл, если число не положительное
    count_positive += 1
    i += 1

# Вывод результата
print(f"Количество положительных чисел в начале последовательности: {count_positive}")
